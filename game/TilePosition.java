package game;

public class TilePosition {



    private int tileX;



    private int tileY;




    public TilePosition(int tileX, int tileY) {

        this.tileX = tileX;
        this.tileY = tileY;
    }




    public int getTileX() {
        return tileX;
    }





    public int getTileY() {
        return tileY;
    }




}
